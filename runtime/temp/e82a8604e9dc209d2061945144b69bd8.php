      			<h2>广告商常见问题</h2>
      <span class="c1">启凡的广告投放到哪些媒体上？</span>
      <p>广告商的广告将直接投放到启凡所有的合作媒体上，且均为世界排名20万以内的网站，这些媒体包括全国性、行业性、地方性的各媒体网站，基本上能覆盖各个地区与行业，超大的访问量，注定超大的浏览点击率，宣传效果可见一斑。</p>
      <span class="c1">启凡的竞价模式是怎样的？</span>
      <p> 竞价，即所有广告商参与，在竞价范围内，设置单价越高，流量越快越多。相同竞价情况下，余额越多，流量越快越多！ </p>
      <span class="c1">关于广告商广告页面的规定</span>
      <p>      放置广告信息的Bannner展示/插屏/点击页面必须是真实页面，不得以任何形式将代码放置于非法页面上，也就是不能通过javascript、ifram、js等已知或未知的方式在其它页面调出，如有此类情况一经查实，我们将撤销该广告并清除该广告剩余投放金额。<br>
	  注： 以上为启凡联盟判定非法广告页面的标准并由发布之日起生效，并严格执行。如发现以上任何违规行为，将撤销其广告项目、没收广告投放金额，情节严重者删除 ID 并永不通过审核。 　<br>
	  启凡有权对以上判定广告商非法广告页面的标准根据实际情况作出相应的修改、补充和完善。 　

