			 <h2>网站主常见问题</h2>
      <span class="c1">域名提交问题的说明（2级米和2级目录）</span>
      <p> 提交域名时只需提交一次根域名即可。例如 http://www.zzz.comm。只需提交zzz.com，其他的2级域名例如http://a.zzz.com或2级目录http://www.zzz.com/b/已自动支持。</p>
      <span class="c1"> 启凡对加盟站有何要求？网站审核未通过的原因？</span>
      <p> 有以下情况者，谢绝加入！ <br />
        1、网站没有实际内容.<br />
		2、网站有病毒或木马.<br />
		3、色情或不健康内容.<br />
		4、非正规投放代码.(隐藏、遮盖广告)<br />
		5、作弊帐号，代理IP或虚假数据.<br />
		6、网站打不开.<br />
		7、网站内容不适合.<br />
		8、其他不遵守启凡传媒相关规定或变相违规的帐号.<br />
        请注意，只审核正规合法网站。<br />
        除以上之外，非自主独立域名，人体艺术、伦理片、三级片、网赚、广告任务、外挂私服、tk域名都不审核!</p>
      <span class="c1">财务密匙是什么?我忘记了怎么办?</span>
      <p> 财务密匙是在启凡注册账号时候填写的一个非常重要的密码，在申请结算和修改财务信息的时候要用到。如果忘记了，请使用留言系统或者联系客服QQ索取，客服会用提示的方法告诉您！ </p>
      <span class="c1">如果我的站点审查时没有被通过，应该怎么办？</span>
      <p> 如果你的帐号状态显示为：审查没通过，或被挂起，这时会有个人通知说明原因，请您修改您的网站后，再进入会员区内，点右侧工具栏的“修改帐号状态/信息”，进行再次提交，或直接与我们的客服QQ联系。 </p>
      <span class="c1">如果我的帐号余额不足100元，能申请结算吗？</span>
      <p>原则上是不可以的。如有特殊情况，可以在周一与我们客服联系。</p>
      <span class="c1">申请提现的标准是多少?多久结算?结算时间?收手续费吗?</span>
      <p>启凡的申请结算的标准为100元，每周支付一次。每周的周六、日可以申请提现，每周一到账！结算不收取任何手续费用！</p>
     
      <span class="c1">网站主帐号状态和下属网站状态的说明！</span><br />
      <p> 
		网站主帐号状态的说明： 审查正常的帐号：点击/显示数据有效，可以申请结算。<br />
		等待审查的帐号：点击/显示数据有效，不能申请结算。<br />
		审查未过的帐号：点击/显示数据有效，不能申请结算。可修改完成后留言或直接联系客服QQ审核账号。 <br />
		被挂起来的帐号：点击/显示数据无效，不能申请结算。相关的一切数据作废。<br />
		下属网站状态的说明：<br />
		网站待审核：新增加网站，等待客服审核状态。此时广告代码无法正常显示，不能正常计费。<br />
		网站已审核：客服已审核正规状态的网站，此时广告代码正常显示。正常计费。<br />
		网站未通过：客服检查之后不能审核的网站。此时广告代码无法正常显示，不能正常计费。未通过网站会在后面备注未通过的原因，网站主可根据原因针对性的修改或提交其他的网站。
		</p>
