      <p> 
		日常检查中发现有以下违规现象时，情况严重的帐号将被挂起！<br />
		一）网站没有实际内容.<br />
		【纯广告页面，新建无内容网站等一律不予审核！】<br />
		二）强制或诱导点击.<br />
		【不得以任何语言或者形式诱导、误导、要求或强制访问者点击广告。】<br />
		违规范例：<br />
		1：支持本站发展，请点击广告。 <br />
		2：点击广告获取下载地址 / 观看影片 / 提高下载速度 / 注册帐号。 <br />
		三）色情或不健康内容.<br />
		【启凡严格审查，坚决杜绝一切色情、人体、露点等不健康网站！】<br />
		四）非正规或未投放代码.<br />
		【注册后长期未投放广告的，启凡有权将账号删除处理。】<br />
		广告条必须完整嵌入网页且完全显示，不得删减、遮挡代码。<br />
		不得将标题颜色和背景颜色使用相近色系，以保证广告的清晰明了。<br />
		六）网站打不开或不适合加入.<br />
		【首页不能正常打开的，或者有任何一种不适合审核的情况的。】<br />
